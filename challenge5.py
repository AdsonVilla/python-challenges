first_number = float(input('Please, type a number: '))
second_number = float(input('Please, type another number: '))

sum = first_number + second_number
subtraction = first_number - second_number
multiplication = first_number * second_number
division = first_number / second_number
potentiation = first_number ** second_number

print(f'A soma entre eles é: {sum}')
print(f'A subtração entre eles é: {subtraction}')
print(f'A multiplicação entre eles é: {multiplication}')
print(f'A divisão entre eles é: {division}')
print(f'O numero {first_number} elevado ao número {second_number} é: {potentiation}')